// Match label to icon key — case insensitive, handles variations
const getIconKey = (label) => {
  const l = (label || "").toLowerCase().trim();
  if (l.includes("fiverr")) return "fiverr";
  if (l.includes("upwork")) return "upwork";
  if (l.includes("linkedin")) return "linkedin";
  if (l.includes("github")) return "github";
  if (l.includes("twitter") || l.includes("x.com") || l === "x")
    return "twitter";
  if (l.includes("instagram")) return "instagram";
  if (l.includes("youtube")) return "youtube";
  if (l.includes("facebook")) return "facebook";
  return null;
};

// Main component — renders real SVG icon or falls back to emoji
export const SocialIcon = ({ label, icon, color, size = 16 }) => {
  const key = getIconKey(label);
  const iconFn = key ? ICONS[key] : null;

  if (iconFn) {
    return (
      <span
        style={{
          display: "flex",
          alignItems: "center",
          justifyContent: "center",
          width: size,
          height: size,
        }}
      >
        {iconFn(color)}
      </span>
    );
  }

  // Fallback — show the emoji from the database
  return (
    <span style={{ fontSize: size * 0.9, lineHeight: 1 }}>{icon || "🔗"}</span>
  );
};
